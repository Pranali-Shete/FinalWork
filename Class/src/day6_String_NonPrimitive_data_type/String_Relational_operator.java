package day6_String_NonPrimitive_data_type;

public class String_Relational_operator {

	public static void main(String[] args) {
//		String comparison using relational operator		==
		
		String h="selenium";
		String i="Selenium";
		String j="SELENIUM";
		String k="selenium";
		
		System.out.println(h==i);//false
		
		System.out.println(h==j);//false
		
		System.out.println(h==k);//true	

	}

}
