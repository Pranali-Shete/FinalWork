package day3_operators_arithmetic;

public class Arithmetic_operator_division {

	public static void main(String[] args) {
		// Division ( sign of Division "/")
		
				//Example:- 1		

						
				int a=100;
				int b=200;
								
				int c=a/b;
								
				System.out.println(c);
								
		//----------------------------------------------------------------------------------------------------------------------		
					
				// Note - We can not take variable a,b & c again for next example. we have to take another variable name.		
								
				//Example:- 2		

						
				int d=100;
				int e=200;
								
				int f=d/e;
								
				System.out.println("Division of d and e is "+f);
										
						
		//----------------------------------------------------------------------------------------------------------------------		
								
				/* Note - We can not take variable a,b,c,d,e & f again for next example. we have to take another variable name. */
										
				//Example:- 3		

								
				int g=100;
				float h=25.55f;
										
				float i=g/h;             // we can't use int here because int show only integer value without decimal but we want integer value with decimal so we have to use float data_type here.

				System.out.println("Division of g and h is "+i);		
						
						
		//----------------------------------------------------------------------------------------------------------------------		
										
				/* Note - We can not take variable a,b,c,d,e,f,g,h & i again for next example. we have to take another variable name. */
															
				//Example:- 4		

													
				int j=100,k=10,l;
										
				l=j/k;
										
				System.out.println("Division of j and k is "+l);			


	}

}
