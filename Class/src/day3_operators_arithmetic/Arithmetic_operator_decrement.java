package day3_operators_arithmetic;

public class Arithmetic_operator_decrement {

	public static void main(String[] args) {
// Decrement ( sign of Decrement  "--")
		
				//Example:- 1		

						
				int a=11;
				System.out.println(a);  // Ans will be 11
								
//-----------------------------------------------------------------------------------------------------		

				//Example:- 2		

				
						int b=11;
						b--;
						System.out.println(b);  // Ans will be 10    ( 11 - 1)
						
//-----------------------------------------------------------------------------------------------------		

				//Example:- 3		

						
						int c=11;
						c=c-2;
						System.out.println(c);  // Ans will be 9    ( value of c is 11 - 2 = 9)
						
			
//-----------------------------------------------------------------------------------------------------		

				//Example:- 4		

								
						int d=50;
						d=d-b;
						System.out.println(d);  // Ans will be 40    ( value of d is 50 + value of b(10) = 40)				
						

	}

}
