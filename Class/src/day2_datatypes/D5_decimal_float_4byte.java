package day2_datatypes;

public class D5_decimal_float_4byte {

	public static void main(String[] args) {
		
		/* float           Memory used 4 byte
		 *      
		 *      syntax:
		 *              datatype variable=value;
		 *              
		 *              e.g
		 *                  float a=20.55f;      To show float value  use f at end of value
		 */		
			
		
		
		float abc=20.00f;
		
		System.out.println(abc);
		
		
		
	}

}
