package day2_datatypes;

public class D9_D1toD8 {

	public static void main(String[] args) {

		
		
		// All are Data types in Java 
		
		/*Notes ---->
		 1) all data types should be in small letters.
		
		
		*/
		
	//	========================================================================================================
		
		// byte ------> Integer
		// short -----> Integer
		// int -------> Integer
		// long ------> Integer
		// float -----> Floating-Point --------> ( only decimal numbers with use f char at end of value like " value is 20 then 20f")
		// double ----> Floating-Point --------> ( all numbers and decimal numbers like Number ( "10,20,5,100") decimal ("10.20,20.45,5.3")
		// char ------> Character -------------> ( any character like "a, b, c d")
		// boolean ---> Boolean   -------------> ( True or False)
		
		
		
		// Syntax of Data types
		
	   //	data_type variable_name = value;
		
		
		byte a=10;
		System.out.println(a);
		
		short b=20;
		System.out.println(b);
		
		int c=30;
		System.out.println(c);
		
		long d=40l;
		System.out.println(d);
		
		float e=50.50f;
		System.out.println(e);
		
		double f=60;
		System.out.println(f);
		
		double g=60.60;
		System.out.println(g);
				
		char i='A';
		System.out.println(i);
		
		boolean j=true;
		System.out.println(j);
		
		boolean k=false;
		System.out.println(k);
		

	}

}
